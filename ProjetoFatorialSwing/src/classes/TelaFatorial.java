    private void spiValStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spiValStateChanged
        // TODO add your handling code here:
        int a = (int) spiVal.getValue();
        String f = a + " x ";
        if (a>1){
            int r = a * --a;
            f += a + " ";
            while (a > 1) {
                r *= --a;
                f += " x " + a;
            }
            lblResult.setText(Integer.toString(r));
        }else{
            lblResult.setText("1");
            f += "1";
        }
        lblFormula.setText(f);
    }//GEN-LAST:event_spiValStateChanged

